package com.test.entity;

public class Users {
    private String Hobby;
    private  String Name;
    private String sex;
    public Users() {
    }
    public String getHobby() {
        return Hobby;
    }
    public void setHobby(String hobby) {
        Hobby = hobby;
    }
    public String getName() {
        return Name;
    }
    public void setName(String name) {
        Name = name;
    }
    public String getSex() {
        return sex;
    }
    public void setSex(String sex) {
        this.sex = sex;
    }
}
